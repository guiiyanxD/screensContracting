    private void eidtarOrdenDeTrabajo(){
        ArrayList<Object[]> orden = new ArrayList<>();
        int cantFilas = jbt_show_orden.getRowCount();
        int cantColumnas = jbt_show_orden.getColumnCount();
        for(int fila = 0; fila < cantFilas; fila++ ){
            Object[] salida = new Object[4];
            for(int colum = 0; colum < cantColumnas; colum++ ){
//                salida[colum] = null;
                salida[colum] = jbt_show_orden.getValueAt(fila, colum);
            }
            orden.add(salida);
        }
        Frm_Principal.contenedor.add(formularioAgregar);
        formularioAgregar.show();
        formularioAgregar.editarOrden(orden, this.nroOrden);
    }

